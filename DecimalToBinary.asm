.MODEL SMALL
.STACK 100H

.DATA
num DW 1

.CODE

MOV AX,@DATA
MOV DS,AX

JMP MAIN

MAIN:

MOV CX,0
MOV AX,num
MOV DL,2

L1:
	DIV DL;  AH = REM, AL = QUO
	MOV BH,0
	MOV BL,AH
	PUSH BX
	INC CX
	
	CMP AL,0
	JE Stop
	
	MOV AH,0
	JMP L1
	
Stop:
	POP DX
	ADD DX,48
	MOV AH,02
	INT 21H

	LOOP Stop

MOV AH,4CH
INT 21H
END